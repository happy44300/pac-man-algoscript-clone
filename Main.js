var GridObject = function(){
  var obj = {};
  obj.x = 0;
  obj.y = 0;
  obj.states = 0;
  obj.size = 0;
  obj.dir = [];
  extend(obj,methods);
  return obj;
};

var extend = function(obj, methods){
  for (var key in methods){
    obj[key] = methods[key];
  }
};

